public class AssignmentOperator {
    public static void main(String[] args) {
        
        int num = 45;
        num += 34;
        System.out.println(num);
        num -= 56;
        System.out.println(num);
        num *= 30;
        System.out.println(num);
        num /= 45;
        System.out.println(num);
        num %= 34;
        System.out.println(num);
    }
}

class RelationalOperator {
    public static void main(String[] args) {
        
        int num1 = 43;
        int num2 = 45;

        System.out.println(num1 < num2);
        System.out.println(num1 > num2);
        System.out.println(num1 == num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 != num2);
    }
    
}
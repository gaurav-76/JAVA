public class ShiftOperator {
    public static void main(String[] args) {
        
        //left shift (number shift_op  number_of_places_to_shift;)
        int a = (10 << 2);  // left shift by 2 places
        System.out.println(a);

        //right shift
        a = (10 >> 2); //right shift by 2 places
        System.out.println(a);
    }
}
